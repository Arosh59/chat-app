// Helper function to get translation
export const getTranslation = (language, path) => {
  const keys = path.split(".");
  let value = translations[language] || translations["en"];

  for (const key of keys) {
    value = value?.[key];
    if (!value) return path; // Return the path if translation not found
  }

  return value;
};

// Helper function to create i18n context value
export const useTranslation = (language = "en") => {
  return {
    t: (path) => getTranslation(language, path),
    language,
  };
};

export default translations;
